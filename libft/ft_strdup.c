#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*cpy;

	i = 0;
	while (str[i])
		i++;
	cpy = NULL;
	cpy = (char *)malloc(sizeof(char) * (i + 1));
	if (cpy == NULL)
		return (0);
	i = 0;
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
