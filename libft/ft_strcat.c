char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (s1[i])
	{
		i++;
	}
	while ((s1[i] = s2[j]))
	{
		i++;
		j++;
	}
	return (s1);
}
