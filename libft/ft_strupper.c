#include "libft.h"

void			ft_strupper(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && *str <= 'z')
			str[i] = str[i] - 32;
		i++;
	}
}
